#include <iostream>

using namespace std;
int main (){
	int v1, v2, suma, resta, multiplicacion, division;
	cout<<"Digite un numero "; cin>>v1;
	cout<<"Digite otro numero "; cin>>v2;
	suma=v1+v2;
	resta=v1-v2;
	multiplicacion=v1*v2;
	division=v1/v2;
	
	cout<<"La suma es: "<<suma<<endl;
	cout<<"La resta es: "<<resta<<endl;
	cout<<"La multiplicación es: "<<multiplicacion<<endl;
	cout<<"La división es: "<<division<<endl;
	return 0;

}

